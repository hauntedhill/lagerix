            {
                List storagesUser = new ArrayList<Integer>();
                storagesUser.add(((StorageDTO)storage.getObj()).getId());
                group.setStorageId(storagesUser);
            }
            List<GroupDTO> groups = new ArrayList<GroupDTO>();
            groups.add(group);
